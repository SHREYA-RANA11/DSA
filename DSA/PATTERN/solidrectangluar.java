public class solidrectangluar {
    public static void main(String[] args) {
        int m = 3;
        int n = 5;
        // row -> 3
        for(int i = 1 ; i <= m ; i++){
            // col -> 5
            for(int j = 1 ; j <= n ; j++){
                System.out.print("* ");
            }
            System.out.println();
        }
    }
}
